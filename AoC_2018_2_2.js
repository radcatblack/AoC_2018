function AreBoxesDifferentByOne (BoxA, BoxB) {
    let index = 0;
    let count = 0;
    let Answer = [];
    let NoAnswer = [];

    while (index < BoxA.length) {
        let currentCharacter = BoxA.charAt(index);
        let compareCharacter = BoxB.charAt(index);

        if (currentCharacter == compareCharacter) {
            index = index +1;
            Answer.push (currentCharacter);
        }

        if (currentCharacter != compareCharacter) {
            count = count +1;
            index = index +1;
        }
    }

    if (count == 1) {
        return Answer;
    }

    if (count != 1) {
        return NoAnswer;
    }

}





