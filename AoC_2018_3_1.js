function StringToClaim (String) {
    let stringGroups = String.split(" ");
    let ClaimID = stringGroups[0];

    let StartParameters = stringGroups[2].split(",");
    let StartLeft = parseInt(StartParameters[0]);
    let StartTop = parseInt(StartParameters[1]);

    let Dimensions = stringGroups[3].split("x");
    let Width = parseInt(Dimensions[0]);
    let Height = parseInt(Dimensions[1]);

    let Claim = {};
    Claim.ClaimID = ClaimID;
    Claim.StartLeft = StartLeft;
    Claim.StartTop = StartTop;
    Claim.Width = Width;
    Claim.Height = Height;

    return Claim;
}


